public class Literals {
    public static void main(String[] args) {
        // Literals
        int num1 = 0b1010;
        int num2 = 0x7E;
        int num3 = 10_00_00_000;
        double num4 = 56;
        double num5 = 12e10;
        System.out.println(num1 + " " + num2 + " " + num3 + " " + num4 + " " + num5);

        char a = 'a';
        System.out.println(++a);

        String str = null;
        System.out.println(str);
    }
}
